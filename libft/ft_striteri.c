#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int n;

	if (!s || !f)
		return ;
	n = 0;
	while (s[n] != '\0')
	{
		(*f)(n, s + n);
		n++;
	}
}
