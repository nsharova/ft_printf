#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*begin;

	begin = (char*)s1;
	while (*s1 != '\0')
		s1++;
	while (*s2 != '\0' && n > 0)
	{
		*s1++ = *s2++;
		n--;
	}
	*s1 = '\0';
	return (begin);
}
